public class RelationalOperator {
    // < > <= >= == !=
    public static void main(String[] args) {
        
        int num = 100;
        int num1 = 200;
        
        System.out.println(num < num1);
        System.out.println(num > num1);
        System.out.println(num <= num1);
        System.out.println(num >= num1);
        System.out.println(num == num1);
        System.out.println(num != num1);
    }
}
